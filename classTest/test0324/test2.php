<?php
    $a=5;            		//$a赋值为5
    echo ++$a;        		//输出6
    echo $a;        		//输出6
    $a=5;
    echo $a++;        		//输出5
    echo $a;        		//输出6
    $a=5;
    echo --$a;        		//输出4
    echo $a;        		//输出4
    $a=5;
    echo $a--;        		//输出5
    echo $a;        		//输出4
?>